//Programme qui affiche l’alphabet en lettres minuscules suivi d’un retour à la ligne
const alphabet = ['a','b','c','d','e','f','g','h','i','j','k','l','m','n','o','p','q','r','s','t','u','v','x','y','z'];
let result = '';
for (var i = 0; i < alphabet.length; i++) {
  result += alphabet[i];
}
console.log(result + "\n");
